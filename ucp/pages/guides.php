							<h2>FAQ</h2>
							<h4>1. How can I bind my keys? Do I need an external program?</h4>
							<p>MTA has its own binding program inside MTA already. So no need for downloading a different program. A guide to bind your keys can be find <a href='http://www.valhallagaming.net/forums/showthread.php?t=47702%5B'/>here</a>.</p>
							<br />						
							<h4>2. My indicators aren't working anymore, how to solve it?</h4>
							<p>When your binds are wiped your indicators won't work any more. You can re-bind them with <a href='http://www.valhallagaming.net/forums/showthread.php?t=53653'/>this guide</a>. Remember that your vehicle must be moving for the indicators to show.<p>						
							<br />						
							<h4>3. My game closes after I press 'Connect'.</h4>
							<p>This problem is probably caused due a corrupt coreconfig.xml. Most of the time it's located in '../Program Files/MTA San Andreas/MTA/'. Delete this file and try to connect.<p>
							<p>WARNING: When you delete your coreconfig.xml file, you will also be deleting any binds you have saved.</p>
							<br />						
							<h4>4. When I make a character and am done, it brings me back to the character selection.</h4>
							<p>If after creating a character it does not save and does not appear in your character list it is you are using invalid characters in the input fields. You can only use letters, numbers, hyphans (-), periods (.) and commas (,).</p>
							<br />						
							<h4>5. When I login I get really bad lag.</h4>
							<p>MTA uses the same graphics settings as single player. The settings you use in SA:MP will not automatically transfer over to MTA. Open single player and configure your Display Options to how you have them in SAMP. Some other steps you should take to reduce lag and ensure your game appears as it should are:
							<ul>
								<li>Remove any modifications you are using.</li>
								<li>Reduce graphics settings in the single player Display Options.</li>
								<li>Select a 32bit resolution in single player or MTA, that is a resolution ending in "32", e.g., 1024 x 768 x 32. If you select a 16bit resolution the colours will appear noticeably off.</li>
							</ul></p>						
