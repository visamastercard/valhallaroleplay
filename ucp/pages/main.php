							<h3>Multi Theft Auto</h3>
							<img src="/images/mta-logo-big.jpg"  align="right" style="padding-left:20px;"/>
							<p>"Multi Theft Auto (MTA) is an open-source software project that adds full on-line gaming support to <a href="http://www.rockstargames.com/sanandreas/">Rockstar North's Grand Theft Auto: San Andreas</a> PC game, in which this functionality is not originally found. It is the first open-source modification that adds a highly customizable network play element to a commercial closed-source single-player PC game.</p>
							<p>Developed as an experimental piece of software for earlier Grand Theft Auto series, Multi Theft Auto has become an advanced multiplayer platform for gamers and developers. The software provides on-line gaming for dozens of players running user-generated gameplay content and scripts through a <a href="http://www.lua.org/">Lua virtual machine</a>, and is freely distributed under the <a href="http://www.opensource.org/licenses/gpl-3.0.html">GPLv3 license</a>."</p>
